# Método 1
# Nesse método, range recebe 3 valores, o primeiro referente a posição de inicio da contagem e o segundo a posição final da contagem e o terceiro ao incremento da função.
# Nesse caso o incremento é 2, portanto range vai realizar a contagem de 2 em 2.
for i in range(0, 20, 2):
	print(i)

